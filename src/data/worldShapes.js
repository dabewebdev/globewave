export function shapeCentroid(shape) {
  const all = shape.polygons.flat();
  if (!all.length) return { lng: 0, lat: 0 };
  let sx = 0, sy = 0;
  for (const [lng, lat] of all) { sx += lng; sy += lat; }
  return { lng: sx / all.length, lat: sy / all.length };
}
